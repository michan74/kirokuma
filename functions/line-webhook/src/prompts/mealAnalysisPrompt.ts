export const MEAL_ANALYSIS_PROMPT = `
この食事の写真を分析して、以下のJSON形式で回答してください。
必ずJSONのみを返してください。説明文は不要です。

{
  "dishes": [
    {
      "name": "料理名",
      "category": "main",
      "ingredients": ["食材1", "食材2"],
      "portion": "medium"
    }
  ],
  "cuisine": "和食",
  "atmosphere": "ヘルシー"
}

=== カテゴリ (category) ===
以下から1つ選択:
- "main": 主菜（メインのおかず。例: 鮭の塩焼き、ハンバーグ、唐揚げ）
- "side": 副菜（サブのおかず。例: サラダ、おひたし、漬物）
- "staple": 主食（例: ご飯、パン、麺、パスタ）
- "soup": 汁物（例: 味噌汁、スープ、お吸い物）

=== 量 (portion) ===
以下から1つ選択:
- "small": 少なめ
- "medium": 普通
- "large": 多め

=== 料理のジャンル (cuisine) ===
料理全体のジャンルを自由に記述してください。
例: "和食", "イタリアン", "中華", "フレンチ", "韓国料理", "和洋折衷", "カフェ飯", "家庭料理"

=== 食事の雰囲気 (atmosphere) ===
食事全体の雰囲気を自由に記述してください。
例: "ヘルシー", "ボリューム満点", "おしゃれ", "家庭的", "豪華", "シンプル", "カジュアル"

=== 注意 ===
- 写真に写っている料理をすべて個別に記録してください
- 1つの料理に対して1つのオブジェクトを作成
- ingredientsは各料理について、実際に使われている食材を1〜3個
`;
